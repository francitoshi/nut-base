    /**
     * Test of hardened method, of class Bip32.
     */
    @Test
    public void testHardened()
    {
        assertEquals(0x80000000, Bip32.hardened(0x80000000));
        assertEquals(0x80000001, Bip32.hardened(0x80000001));
        assertEquals(0x80000000, Bip32.hardened(0));
        assertEquals(0x80000001, Bip32.hardened(1));   
    }

    /**
     * Test of unhardened method, of class Bip32.
     */
    @Test
    public void testUnhardened()
    {
        assertEquals(0, Bip32.unhardened(0));
        assertEquals(1, Bip32.unhardened(1));
        assertEquals(0, Bip32.unhardened(0x80000000));
        assertEquals(1, Bip32.unhardened(0x80000001));
        assertEquals(0x1111111, Bip32.unhardened(0x81111111));
    }

    /**
     * Test of isHardened method, of class Bip32.
     */
    @Test
    public void testIsHardened()
    {
        assertFalse(Bip32.isHardened(0));
        assertFalse(Bip32.isHardened(1));
        assertTrue(Bip32.isHardened(0x80000000));
        assertTrue(Bip32.isHardened(0x80000001));
        assertTrue(Bip32.isHardened(0x81111111));
    }

    /**
     * Test of parse method, of class Bip32.
     */
    @Test
    public void testParse() throws Exception
    {
        for (String[][] data : DATA)
        {
            for (String[] vector : data)
            {
                for (String item : vector)
                {
                    if(item.startsWith("xpub") || item.startsWith("xprv"))
                    {
                        ExtKey key = Bip32.parse(item);
                        assertEquals(item, key.toString());
                    }
                }
            }
        }
    }

    /**
     * Test of parsePath method, of class Bip32.
     */
    @Test
    public void testParsePathFormatPath()
    {
        for (String[][] data : DATA)
        {
            for (String[] vector : data)
            {
                for (String item : vector)
                {
                    if(item.startsWith("m"))
                    {
                        int[] path = Bip32.parsePath(item);
                        assertEquals(item.toUpperCase(), Bip32.formatPath(path).toUpperCase());
                    }
                }
            }
        }
    }
    
